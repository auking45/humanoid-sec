export const generateTargetSecurityReport = (target: Target, checklist: Checklist, theme: Theme, aiAnalysis?: any) => {
  const doc = new jsPDF();
  const primaryRgb = hexToRgb(theme.primary);
  const now = new Date();
  const timestamp = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')} ${String(now.getHours()).padStart(2, '0')}:${String(now.getMinutes()).padStart(2, '0')}:${String(now.getSeconds()).padStart(2, '0')}`;
  
  // Fancy Header
  doc.setFillColor(primaryRgb[0], primaryRgb[1], primaryRgb[2]);
  doc.rect(0, 0, 210, 40, 'F');
  
  doc.setFontSize(22);
  doc.setTextColor(255, 255, 255);
  doc.text('Security Analysis Report', 14, 25);
  
  doc.setFontSize(9);
  doc.text(`Generated: ${timestamp}`, 150, 25);

  // Target Info
  doc.setFontSize(18);
  doc.setTextColor(30, 41, 59);
  doc.text(target.name, 14, 55);
  
  doc.setFontSize(12);
  doc.setTextColor(100, 116, 139);
  doc.text(`Type: ${target.type}`, 14, 62);
  doc.text(target.description || '', 14, 70, { maxWidth: 180 });

  // Score Section
  const score = 100 - target.riskScore;
  const status = target.riskScore > 70 ? 'CRITICAL' : target.riskScore > 40 ? 'WARNING' : 'SECURE';
  const statusColor = target.riskScore > 70 ? [225, 29, 72] : target.riskScore > 40 ? [217, 119, 6] : [5, 150, 105];

  doc.setDrawColor(226, 232, 240);
  doc.setLineWidth(0.5);
  doc.line(14, 80, 196, 80);

  doc.setFontSize(14);
  doc.setTextColor(30, 41, 59);
  doc.text('Security Posture Summary', 14, 95);

  doc.setFontSize(48);
  doc.setTextColor(statusColor[0], statusColor[1], statusColor[2]);
  doc.text(`${score}%`, 14, 120);
  
  doc.setFontSize(14);
  doc.text(`Status: ${status}`, 60, 115);
  doc.setFontSize(10);
  doc.setTextColor(100, 116, 139);
  doc.text('Based on weighted security control verification.', 60, 122);

  // Findings Table
  doc.setFontSize(14);
  doc.setTextColor(primaryRgb[0], primaryRgb[1], primaryRgb[2]);
  doc.text('Detailed Findings', 14, 140);

  const results = target.checklistResults[checklist.id] || {};
  const findingsData = checklist.items.map(item => {
    const res = results[item.id];
    const completed = typeof res === 'boolean' ? res : res?.checked;
    const justification = typeof res === 'object' ? res?.justification : '';
    const status = typeof res === 'object' ? res?.reviewStatus : 'pending';
    
    let resultText = completed ? 'PASSED' : 'FAILED';
    if (!completed && status === 'approved') {
      resultText = 'EXCEPTED (Approved)';
    } else if (!completed && status === 'rejected') {
      resultText = 'FAILED (Rejected)';
    }

    return [
      item.text,
      item.category,
      item.weight.toString(),
      resultText,
      justification || 'N/A'
    ];
  });

  autoTable(doc, {
    startY: 145,
    head: [['Control', 'Category', 'Weight', 'Result', 'Justification']],
    body: findingsData,
    theme: 'grid',
    headStyles: { fillColor: primaryRgb },
    columnStyles: {
      0: { cellWidth: 50 },
      1: { cellWidth: 30 },
      2: { cellWidth: 20 },
      3: { cellWidth: 30 },
      4: { cellWidth: 50 }
    },
    didParseCell: (data) => {
      if (data.section === 'body' && data.column.index === 3) {
        if (data.cell.text[0] === 'PASSED') {
          data.cell.styles.textColor = [5, 150, 105];
          data.cell.styles.fontStyle = 'bold';
        } else if (data.cell.text[0] === 'EXCEPTED (Approved)') {
          data.cell.styles.textColor = primaryRgb;
          data.cell.styles.fontStyle = 'bold';
        } else {
          data.cell.styles.textColor = [225, 29, 72];
          data.cell.styles.fontStyle = 'bold';
        }
      }
    }
  });

  // AI Recommendations if available
  if (aiAnalysis) {
    const finalY = (doc as any).lastAutoTable.finalY + 20;
    if (finalY > 230) doc.addPage();
    
    doc.setFontSize(14);
    doc.setTextColor(primaryRgb[0], primaryRgb[1], primaryRgb[2]);
    doc.text('AI-Powered Recommendations', 14, finalY > 230 ? 20 : finalY);
    
    doc.setFontSize(10);
    doc.setTextColor(30, 41, 59);
    const recs = aiAnalysis.recommendations || [];
    let recY = (finalY > 230 ? 30 : finalY + 10);
    
    recs.forEach((rec: string, i: number) => {
      doc.text(`• ${rec}`, 14, recY, { maxWidth: 180 });
      recY += 7;
    });
  }

  doc.save(`${target.name.replace(/\s+/g, '_')}_Security_Report.pdf`);
};
