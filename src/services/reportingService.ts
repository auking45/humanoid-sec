import { Checklist, Target, Theme } from '../types';
import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';

const hexToRgb = (hex: string): [number, number, number] => {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return [r, g, b];
};

const getWeightEmoji = (weight: number) => {
  if (weight >= 5) return '🔴 CRITICAL';
  if (weight >= 4) return '🟠 HIGH';
  if (weight >= 3) return '🟡 MEDIUM';
  if (weight >= 2) return '🔵 LOW';
  return '⚪ INFO';
};

const getCategoryEmoji = (category: string) => {
  const cat = category.toLowerCase();
  if (cat.includes('system')) return '💻';
  if (cat.includes('os')) return '🐧';
  if (cat.includes('network')) return '🌐';
  if (cat.includes('cloud')) return '☁️';
  if (cat.includes('software')) return '📦';
  if (cat.includes('access')) return '🔑';
  if (cat.includes('physical')) return '🏗️';
  if (cat.includes('audit')) return '📜';
  return '📁';
};

export const exportChecklistToMarkdown = (checklist: Checklist): string => {
  let md = `# 🛡️ Security Checklist: ${checklist.title}\n\n`;
  md += `> ${checklist.description}\n\n`;
  md += `---\n\n`;
  
  const categories = Array.from(new Set(checklist.items.map(i => i.category)));
  
  categories.forEach(cat => {
    md += `## ${getCategoryEmoji(cat)} ${cat}\n\n`;
    const items = checklist.items.filter(i => i.category === cat);
    items.forEach((item, idx) => {
      md += `### 🔍 ${idx + 1}. ${item.text}\n`;
      md += `**Impact Weight:** ${getWeightEmoji(item.weight)} (${item.weight}/5)\n\n`;
      if (item.description) {
        md += `> 📝 **Detailed Requirements:**\n`;
        md += `> ${item.description.replace(/\n/g, '\n> ')}\n\n`;
      }
      md += `---\n\n`;
    });
  });
  
  md += `_Generated by Robot Security Fleet Manager_\n`;
  return md;
};

