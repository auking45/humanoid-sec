export const downloadMarkdown = (filename: string, content: string) => {
  const element = document.createElement('a');
  const file = new Blob([content], { type: 'text/markdown' });
  element.href = URL.createObjectURL(file);
  element.download = `${filename}.md`;
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
};

export const generateChecklistPDF = (checklist: Checklist, theme: Theme) => {
  const doc = new jsPDF();
  const primaryRgb = hexToRgb(theme.primary);
  
  // Header
  doc.setFontSize(22);
  doc.setTextColor(primaryRgb[0], primaryRgb[1], primaryRgb[2]);
  doc.text('Security Checklist Report', 14, 22);
  
  doc.setFontSize(16);
  doc.setTextColor(30, 41, 59); // Slate-800
  doc.text(checklist.title, 14, 32);
  
  doc.setFontSize(10);
  doc.setTextColor(100, 116, 139); // Slate-500
  doc.text(checklist.description, 14, 40, { maxWidth: 180 });

  let currentY = 55;

  const categories = Array.from(new Set(checklist.items.map(i => i.category)));
  
  categories.forEach(cat => {
    if (currentY > 250) {
      doc.addPage();
      currentY = 20;
    }
    
    doc.setFontSize(14);
    doc.setTextColor(primaryRgb[0], primaryRgb[1], primaryRgb[2]);
    doc.text(cat, 14, currentY);
    currentY += 10;

    const items = checklist.items.filter(i => i.category === cat);
    const tableData = items.map((item, idx) => [
      (idx + 1).toString(),
      item.text,
      item.weight.toString(),
      item.description || 'N/A'
    ]);

    autoTable(doc, {
      startY: currentY,
      head: [['#', 'Requirement', 'Weight', 'Details']],
      body: tableData,
      theme: 'striped',
      headStyles: { fillColor: primaryRgb },
      columnStyles: {
        0: { cellWidth: 10 },
        1: { cellWidth: 60 },
        2: { cellWidth: 20 },
        3: { cellWidth: 90 }
      },
      margin: { left: 14, right: 14 }
    });

    currentY = (doc as any).lastAutoTable.finalY + 15;
  });

  doc.save(`${checklist.title.replace(/\s+/g, '_')}_Checklist.pdf`);
};

